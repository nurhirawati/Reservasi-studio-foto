    private void jButton_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegisterActionPerformed
        // TODO add your handling code here:
        
    if (jPasswordField1.getText().equals(jPasswordField2.getText())) {
        fullname = jTextField1.getText();
        username = jTextField2.getText();
        password = jPasswordField1.getText();

        // Melakukan koneksi ke database
        Connection connection = null;
        try {
            String url = "jdbc:mysql://localhost:3306/reservasi_studio_foto";
            String user = "root";
            String dbPassword = "";

            connection = DriverManager.getConnection(url, user, dbPassword);

            // Query untuk menyimpan data ke tabel "user" tanpa memasukkan nilai ID user
            String query = "INSERT INTO user (fullname, username, password) VALUES (?, ?, ?)";
            PreparedStatement preparedStatement = connection.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
            preparedStatement.setString(1, fullname);
            preparedStatement.setString(2, username);
            preparedStatement.setString(3, password);

            int rowsInserted = preparedStatement.executeUpdate();
            if (rowsInserted > 0) {
                ResultSet generatedKeys = preparedStatement.getGeneratedKeys();
                if (generatedKeys.next()) {
                    int generatedID = generatedKeys.getInt(1);
                    System.out.println("User registered successfully! ID: " + generatedID);
                    // Lakukan sesuatu dengan nilai ID yang di-generate, jika diperlukan
                }
            }
        } catch (SQLException e) {
            System.out.println("Failed to register user: " + e.getMessage());
        } finally {
            try {
                if (connection != null) {
                    connection.close();
                }
            } catch (SQLException ex) {
                System.out.println("Failed to close connection: " + ex.getMessage());
            }
        }
        
        Login lg = new Login ();
        lg.setVisible(true);
        lg.pack();
        lg.setLocationRelativeTo(null);
        lg.setDefaultCloseOperation(Register.EXIT_ON_CLOSE);
    } else {
        jLabel7.setText("The password doesn't match");
    }
    
    }//GEN-LAST:event_jButton_RegisterActionPerformed

